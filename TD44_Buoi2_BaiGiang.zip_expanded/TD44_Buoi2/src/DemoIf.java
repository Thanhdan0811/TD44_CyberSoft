
public class DemoIf {

	public static void main(String[] args) {
		/*
		 * Demo If
		 */
		
		/*
		 * Cú pháp: if(<biểu thức điều kiện>){
		 * 				hành-động
		 * 			}
		 */
		
		boolean troiMua = true;
		if(troiMua) {
			System.out.println("Nhớ mang theo áo mưa nhé.");
		}
		
		System.out.println("Kết thúc chương trình.");
	}

}
